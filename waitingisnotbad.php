                                                <p>For some businesses, long lineup aren’t necessarily a bad thing</p>
                                                <p>The commonly accepted mantra is that long lineups are bad. They can take away from the customer experience, frustrate customers, and may cause potential customers to just walk away . Who wants to spend any time waiting in a line? </p>
                                                <p>But for some businesses and events, long lineups can actually create the appearance of popularity and perceived value. Generally, it is a best practice to create the shortest line possible, but if you’re trying to position your business or long lineups are unavoidable, you can use it to your advantage. </p>
                                                <p>Ultimately, it is important to realize that the main issue with lineups is not simply about how long people are waiting in it, rather it is whether what they receive at the end of the lineup is going to be worth the wait. If customers feel that what awaits them at the end of the line is something of value, then they will have no problem waiting for hours on end to get what they want. </p>
                                                <p>So I took the time to analyze several unique businesses in order illustrate that long line ups aren’t necessarily an evil which needs to be cleansed from the world. It’s important to focus on what these businesses are offering their customers opposed to the length of the line ups. By examining these case studies, you might be able to take away a few lessons that you can apply to your own queues and crowd control. </p>
                                                <h2>Uncle Tetsu’s</h2>
                                                <p>Uncle Tetsu’s is located in Downtown Toronto. It has positioned itself as the go to location for Japanese Cheesecake. Their standard cheesecake has gotten rave reviews, and their store is a must-stop destination if visiting Toronto. In contrast to typical 5 minutes bakery, the line up to buy one of Uncle Tetsu’s cheesecakes can easily stretch into a 20-30 minute wait. </p>
                                                <p>So why do so many people wait so long in the cold Toronto weather for a $10 cake? I would argue that this springs from the idea that these Uncle Tetsu’s cakes are unique and one of a kind. In the creation of their premium product, the shop will only produce a dozen cakes at a time, baking them right in front of you. This does two things. People see that the cakes are fresh and handmade. The other is that it creates an unreasonably long lineup. </p>
                                                <p>I don’t even think that many people mind waiting in line. When people start talking about Uncle Tetsu’s guess what is always part of the conversation? Of course the first thing that everyone talks about is the light, fluffy, and delicate cheesecake that they bought. The second part? How long you had to wait for that cheesecake. </p>
                                                <p>Normally, most people wouldn’t bother waiting for 20 minutes to buy cake, but these long lineups have actually helped Uncle Tetsu’s to market and position itself. In conversation, waiting is considered a sign of accomplishment, a badge of honour, that only adds to the value of the cake. Another effect of waiting is that people walking in front of the store naturally become very curious about why people are lining up outside of a store. When people see such a long lineup, they assume that it means that the product must be exclusive and worth waiting for. </p>
                                                <p>Uncle Tetsu’s is a perfect example of creating the illusion of scarcity, when a product appears to be rare. Without a doubt, their cakes taste fantastic, but they taste even better because you have to wait so long for them. </p>
                                                <h2>Alley Bubble Tea</h2>
                                                <p>Another example of the premium approach is a bubble tea shop called the Alley. It’s a chain known for its handcrafted tapioca, unique flavours, and a premium experience. </p>
                                                <p>There are raving Yelp reviews on the Alley about the quality of the bubble tea. Right beside those reviews are comments on the long wait. While customers would obviously prefer to avoid the wait entirely, The Alley makes the whole waiting period acceptable because they are offering a premium product that everyone wants. </p>
                                                <p>Looking at both Uncle Tetsu and The Alley, we can observe a common trend with the idea of a premium product or service. We can see that the much hated queue is not always a bad practice. As a business, its important to consider whether creating a lineup can help add to the perceived value of your entire experience.
                                                <p>Of course, keeping a lineup short is the best thing for the customer experience. The takeaway from these two stores is that your business or event needs to focus on creating a quality product or service that people are just begging to line up for. What are you waiting for?</p>

