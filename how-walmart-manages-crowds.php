                                                <p>Toronto International Film Festival. SunWing Airlines. Walmart. These are all huge businesses and event organizers that have to deal with huge crowds. Have you ever seen the paparazzi at the TIFF? How about the travellers during Christmas time? One thing that you must have seen at least once in your life has been the crazy Black Friday hordes in Walmart. </p>
                                                <p>All of these businesses have to be prepared to deal with huge influxes of customers or people. What that means is that we can all learn a few lessons from them when it comes to managing our own events and ensuring an optimal experience for all of our customers.</p>

                                                <h2>Prep your staff</h2>
                                                <p>One of the most significant sticking points at many events has always been the staff. A few common problems such as inefficiency, lack of knowledge, and poor communication are often easily avoidable. </p>
                                                <p>
                                                    One of the most important aspects that all of these big businesses perform is that they train their staff. TIFF staff know how to handle a situation where an attendee has forgotten their ticket. SunWing has set procedures to follow in terms of handling oversize baggage. Walmart employees are trained on how to use their cash register and alter transactions. </p>
                                                <p>Even though you may be running a small business or event, it’s important to realize that a little bit of training for your employees is time well invested and can go a very long way. It’s not enough to assume that your employees will just know how to manage their events, when you have a set of customers to please. You need to manage your risk, and that’s a trick that all of these established organizations realize. </p>
                                                <p>
                                                    A few starter points for training your employees may be something as simple as making sure that they know what location they have to be and their roles. Have you walked them through a list of common situations or concerns that often come up with your customers? Do they know answers to common questions?</p>
                                                <p>Another important aspect to consider is whether they know the purpose of the event, so that if anything falls outside of the scope of their training, they can be independent and make their own decisions.</p>

                                                <h2>Design effective lineups</h2>
                                                <p>One of the keys to managing large crowds is creating an effective, and controlled queue. A lineup is key to creating a controlled customer experience that will ensure the smooth running of your event. Just because a crowd is large doesn’t mean that it is not possible to create some order in the chaos.</p>
                                                <p>A common tool that all three organizations use to outline and create their queues are stanchions. These posts connect to each other with belts to create an organized layout for their lines.</p>
                                                <p>In terms of organization and layout of these stanchions, it all depends on factors such as space, crowd size, and needs. </p>
                                                <p>For example, the TIFF may have a single line for tickets, but create an “express line” for VIP’s. In contrast, due to the limited space in the stores, and the need for fair treatment of its customers, Walmart has several lineups at different cashiers. </p>
                                                <p>As a general rule for you business, having one line is more efficient to reduce wait times. However, it is even more important that you invest time into experimenting with different layouts until you find the best one for your business. You can find more details about crowd control layouts in this post <a href="effective-stanchion-layouts.php">here</a></p>

                                                <h2>Use Data to predict</h2>
                                                <p> In this age of technology, it has never been easier to find some technology to solve all your problems. Crowd control and lineups are a field that are no exception.</p>
                                                <p>Large corporations such as SunWing may invest into hefty ticket systems that allow them to predict demand for a flights ahead of time. This lets them find the right amount of staff to put on a shift in order to meet the needs of the crowd. However, with today’s technology platforms such as Facebook and eventBrite, you can gain a lot of these crowd management functionality without shelling out a significant amount of cash.</p>
                                                <p>For casual events, the use of Facebook is often all smaller businesses need to predict that amount of traffic that their event can expect. Any organization can start a Facebook Page and create a Facebook event where the public can respond whether they are going to attend or not. While it’s important to realize that these numbers may not be fully accurate, they usually provide enough information in order to make informed decisions on staff and inventory.</p>
                                                <p>TIFF actually uses Facebook events to market their film screenings. But it’s also an effective way for them to predict the expected audience and prep accordingly </p>
