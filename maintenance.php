                                                <h2>Maintenance and Durability</h2>
                                                <p>Stanchions are fairly low maintenance tools for your business. Usually you can just “set it and forget it” for crowd control. However, to maximize the longevity and use of stanchions, there are a few standard practices that can be implemented. On average, entry-level and premium level stanchions may last three and ten years respectively, but good maintenance of these stanchions can help your stanchions go far beyond these standard lifetimes.</p>

                                                <h2>Cassette Heads and Belts</h2>
                                                <p>The cassette head is a spring loaded device that stores and retracts the belt. To ensure that the cassette head of your stanchion does not wear out as quickly, it is crucial that you avoid straining or damaging the cassette head by attempting to over extend the belt which can damage the spring.</p>
                                                <p>Another common mispractice is to quickly unhook the belts from connectors and let go. While it may be amusing to watch the belt wind up, this results in a quick retraction that also has potential to damage the stanchion belt and lower the lifespan of the product.</p>
                                                <p>These two practices often occur during the setup and takedown of these stanchions. Make sure that staff and employees understand the damage that can be caused by overextending and quick retraction. Often, all it takes is a quick 2 minute reminder that can prevent damage that takes years off the lifespan of the product.</p>
                                                <p>Should you ever reach a point where the cassette head becomes damaged, they can be removed and easily replaced. There is no need to spend another $100+ on buying another stanchion post. You can often find standard replacements with will work with a variety of brands with the original seller of the stanchion. A sample of these cassette heads can be found here.</p>
                                                <h2>Keep the Stanchions Dry</h2>
                                                <p>Often, the stanchions will have stainless steel exterior that will not rust. However, it is common for the bolts, interior, and base to be made out of materials that will rust through when exposed to excess water. To ensure the durability of the product, it is important to dry off stanchions if they are exposed to any moisture or water during their use.</p>
                                                <p>Rust is a concern because it increases the brittleness of the metal, eventually leading to a point where the stanchion will start leaning or fail to support its own weight. In addition, if the rust begins to show, it makes for a poor impression with customers who associate your business with shoddiness and carelessness.</p>
                                                <p>Rust is especially a concern for businesses or events who are placing their stanchions outdoors where they will be exposed to the elements. A good practice is to quickly wipe down these stanchions with a cloth to delay the onset of rusting.</p>
                                                <h2>Tightening the Pole and Base</h2>
                                                <p>Stanchion posts are attached to the base with a series of bolts. It is possible that as the stanchions becomes more frequently used, the bolts that attach these two components will loosen. This may cause the post to become shaky or lean at an angle. In general, loose bolts will decrease the overall stability of the stanchion, or in extreme situations, detach the pole and the base.</p>
                                                <p>In general, loosened bolts are one of the easier problems to fix. One can simply locate them at the bottom of the post and tighten them with a wrench in order to secure the post to the base.</p>

